if HAVE_MODAL:
    image = (
        modal.Image.debian_slim(python_version="3.11")
        .pip_install("torch", "transformers", "accelerate", "numpy", "tqdm", "safetensors")
    )
    app = modal.App(APP_NAME, image=image)
    vol = modal.Volume.from_name(VOL_NAME, create_if_missing=True)

    @app.function(
        gpu="A10G",
        timeout=1800,
        volumes={"/outputs": vol},
    )
    def run_seed(seed: int) -> dict:
        out_dir = pathlib.Path("/outputs")
        out_dir.mkdir(exist_ok=True)
        res = run_seed_body(seed, out_dir)
        vol.commit()
        return res

    @app.local_entrypoint()
    def main(seeds: str = "0,1,2"):
        seed_list = [int(s) for s in seeds.split(",")]
        results = list(run_seed.map(seed_list))
        primary = [r["primary_metric"] for r in results]
        mean = sum(primary) / len(primary)
        std = (sum((p - mean) ** 2 for p in primary) / len(primary)) ** 0.5
        summary = {
            "primary_metric": {
                "name": results[0]["primary_metric_name"],
                "mean": mean, "std": std, "n_seeds": len(primary),
            },
            "per_seed": results,
        }
        with vol.batch_upload(force=True) as up:
            up.put_bytes(json.dumps(summary, indent=2).encode(), "/summary.json")
        print(f"SUMMARY: {json.dumps(summary)}")


